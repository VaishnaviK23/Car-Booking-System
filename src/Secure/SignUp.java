    private void txtRepassFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtRepassFocusLost
        // TODO add your handling code here:
        char[] pass, repass;
        pass = txtPasswd.getPassword();
        repass = txtRepass.getPassword();
        if (!(Arrays.equals(pass, repass))) {
            JOptionPane.showMessageDialog(null, "Passwords do not match!");
            txtPasswd.setText("");
            txtRepass.setText("");
        }
    }//GEN-LAST:event_txtRepassFocusLost

