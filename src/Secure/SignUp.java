    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String Fname, Lname, Uname, Stname, City, State, Pincode, Mnumber, Email, password, Gender;
        conn = MySqlConnect.ConnectDB();
        String Sql1 = "Insert into customer values(?, ?, ?, ?)";
        String Sql2 = "Insert into name values(?, ?, ?)";
        String Sql3 = "Insert into address values(?, ?, ?, ?, ?)";
        String Sql4 = "Insert into login_details values(?, ?)";
        Fname = txtFname.getText();
        Lname = txtLname.getText();
        Uname = txtUname.getText();
        Stname = txtStreetname.getText();
        City = txtCity.getText();
        State = txtState.getText();
        Pincode = txtPcode.getText();
        Mnumber = txtMnumber.getText();
        Email = txtEmail.getText();
        password = txtRepass.getText();

        if (Fname.equals("") || Lname.equals("") || Stname.equals("") || City.equals("") || Pincode.equals("") || State.equals("") || Mnumber.equals("") || Email.equals("") || password.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter all details!");
        } else {
            // Gender = ((comGender.getSelectedIndex() == 0)?"Male" : "Female");
            Gender = comGender.getSelectedItem().toString();
            try {
                pst = conn.prepareStatement(Sql1);
                pst.setString(1, txtUname.getText());
                pst.setString(2, txtMnumber.getText());
                pst.setString(3, Gender);
                pst.setString(4, txtEmail.getText());
                int i = pst.executeUpdate();
                pst = conn.prepareStatement(Sql2);
                pst.setString(1, txtUname.getText());
                pst.setString(2, txtFname.getText());
                pst.setString(3, txtLname.getText());
                i = pst.executeUpdate();
                pst = conn.prepareStatement(Sql3);
                pst.setString(1, txtUname.getText());
                pst.setString(2, txtStreetname.getText());
                pst.setString(3, txtCity.getText());
                pst.setString(4, txtPcode.getText());
                pst.setString(5, txtState.getText());
                i = pst.executeUpdate();
                pst = conn.prepareStatement(Sql4);
                pst.setString(1, txtUname.getText());
                pst.setString(2, txtRepass.getText());
                i = pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Sign Up successful!!");
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

